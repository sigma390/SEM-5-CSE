	AREA tw6,CODE,READONLY
		ENTRY
		MOV R8,#4
		LDR R1,=CVALUE
		LDR R2,=DVALUE
		
LOOP1	LDR R3,[R1],#4
		STR R3,[R2],#4
		SUBS R8,R8,#1
		CMP R8,#0
		BNE LOOP1
		
START	MOV R7,#0;SWAP FLAG IS CLEARED
		MOV R5,#3;COMPARISON COUNT
		LDR R2,=DVALUE
LOOP3	LDR R1,[R2],#4;1ST NO IN R1
		LDR R3,[R2];2ND NO IN R3
		CMP R1,R3
		BLT LOOP2
		
		STR R1,[R2],#-4
		STR R3,[R2]
		MOV R7,#1;SWAP=1
		ADD R2,#4;RESTORE POINTER
		
LOOP2	SUBS R5,R5,#1
		CMP R5,#0
		BNE LOOP3
		
		CMP R7,#0
		BNE START
L		B L
CVALUE DCD 0X44444444,0X33333333,0X22222222,0X11111111
	AREA MYDATA,DATA,READWRITE
DVALUE DCD 0
	END